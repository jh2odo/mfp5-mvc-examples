        <h2 class="titulo">&iquest; Cómo uso este sistema en mi navegador ?</h2>

        <p>La mayoría de los navegadores son compatibles con los accesskeys.</p>

        <p>Combinaciones específicas de los navegadores:</p>
        <ul>
            <li><strong>Alt + Accesskey + tecla &quot;Enter&quot;</strong> <br/>
                en Internet Explorer para Windows
            </li>
            <li><strong>Alt + Accesskey</strong><br/>
                en Firefox para Windows Ver. 1.5 e inferiores, <br/>
                en Mozilla para Windows y en Netscape Ver. 6 y superiores para Windows<br/>
                Galeon, Mozilla y FireFox para Linux
            </li>
            <li><strong>Alt + Shift + Accesskey</strong> <br/>
                en Firefox Ver. 2 para Windows
            </li>
            <li><strong>Shift + Esc + Accesskey</strong> <br/>
                en Opera para Windows o Mac
            </li>

            <li><strong>Ctrl + Accesskey </strong><br/>
                en Safari Ver. 1.2 para Mac, <br/>
                en Firefox para Mac (incluyendo Firefox Ver. 2), <br/>
                en Mozilla para Mac, <br/>
                en Netscape Ver. 6 y superiores para Mac<br/>
                en Internet Explorer para Mac<br/>
                en Konqueror 3.3 y superiores para Linux
            </li>
            <li><strong>No son compatibles con las accesskeys</strong><br/>
                Netscape 4, Camino, Galeon, Konqueror versiones anteriores a
                3.3.0, Omniweb, Safari versiones anteriores a 1.2, Opera
                Windows/Linux versiones anteriores a la 7
            </li>

        </ul>

